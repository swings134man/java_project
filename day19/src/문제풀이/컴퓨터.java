package 문제풀이;

public class 컴퓨터 {

	int price;
	String com;
	int size;
	public 컴퓨터(int price, String com, int size) {
		this.price = price;
		this.com = com;
		this.size = size;
	}
	public String toString() {
		return "컴퓨터 [가격= " + price + ", 제조회사= " + com + ", 모니터크기= " + size + "]";
	}
	
	
	
	
	
}
